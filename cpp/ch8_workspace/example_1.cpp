/*
 기본 배열 예제
 built-in array
 */

#include <iostream>
using namespace std;

int main() {
    int arr[5];
    
    arr[0] = 1;
    arr[1] = 2;
    arr[2] = 3;
    arr[3] = 4;
    arr[4] = 5;
    
    cout << arr[0] << endl;
    cout << arr[1] << endl;
    cout << arr[2] << endl;
    cout << arr[3] << endl;
    cout << arr[4] << endl;
    cout << arr[5] << endl; // 배열의 잘못된 사용
    
    
}
